            <h2 class="sub-h2" style="margin-top: 12px;"> We help foreigners to prepare and file documents for the following residence permits:</h2>
            <p class="subtittl">
                <strong>Temporary/Working residence permit</strong>&nbsp;is issued to persons coming to Georgia for entrepreneurial or labour activities;
            </p>

            <p class="subtittl">
                <strong>Permanent (Investment) residence permit</strong> is issued to a foreign citizen who invested at least 300 000 US dollars in Georgia. this provision is prescribed in the Law of Georgia on Promotion and Guarantees of investment. in such cases, residence permits are also issued to the investor’s family members, which include a spouse, underage children and/or fully dependent incapacitated person;
            </p>

            <p class="subtittl">
                <strong>Short-term residence permit</strong> is issued to the person – and his/her family members – who, according to the rule established by Georgian legislation have the right to immovable property (except for agricultural land), with the market price above 100 000 US dollars;
            </p>

            <p class="subtittl">
                <strong>Tax residency</strong>&nbsp;is issued to a foreign citizen (high net worth individual) without living in Georgia for
                185 days, in case the value of his/her worldwide property exceeds 3 000 000 GEL (around 1 200 000
                USD) or his/her annual income has exceeded 200 000 GEL (during the last three years). Also the High
                Net Worth Individual should have Georgian residence permit or his/her Georgian sourced income
                should exceed 25 000 GEL during a single year.
            </p>
