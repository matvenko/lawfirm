            <h2 class="sub-h2" style="margin-top: 12px;"> REGISTER YOUR BUSINESS IN GEORGIA</h2>

            <p>There are three main business structures in Georgia:</p>

            <h2 class="sub-h2" style="margin-top: 12px;">  LIMITED LIABILITY COMPANY (LLC/LTD) </h2>
            <p>
                Limited liability company is the most convenient legal form of business activity in Georgia. A Limited Liability Company (LLC)
                is a commercial legal entity, the liability of which to its creditors is limited to its entire property. An LLC may be established by a single person.
            </p>

            <h2 class="sub-h2" style="margin-top: 12px;"> JOINT STOCK COMPANY (JSC) </h2>
            <p>
                A Joint Stock Company (JSC) is a legal entity the charter capital of which is divided into shares of equal nominal value.
                The share constitutes to a security certifying the claim of a shareholder in relation to the property of the JSC,
                the rights and obligations of shareholders and JSC towards one another. The liability of a JSC is limited to its whole assets.
                Shareholders are not liable for obligations of the JSC.
            </p>

            <h2 class="sub-h2" style="margin-top: 12px;"> BRANCH OFFICE (BO) </h2>
            <p>
                Any enterprise may establish a Branch Office (BO). It is the sub-unit of a business entity and is not a separate legal entity.
                Therefore, the liability of its founder may not be limited to the assets of the branch itself. The latter denotes that the founder
                is financially liable for the obligations of the branch.
            </p>

